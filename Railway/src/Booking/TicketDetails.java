package Booking;

public class TicketDetails {
	public int ticketNumber;
	public String name;
	public int age;
	public String gender;
	public String BearthPreference;
	public String BearthAllocated;
	public String status;
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getTicketNumber() {
		return ticketNumber;
	}
	public void setTicketNumber(int ticketNumber) {
		this.ticketNumber = ticketNumber;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getBearthPreference() {
		return BearthPreference;
	}
	public void setBearthPreference(String bearthPreference) {
		BearthPreference = bearthPreference;
	}
	public String getBearthAllocated() {
		return BearthAllocated;
	}
	public void setBearthAllocated(String bearthAllocated) {
		BearthAllocated = bearthAllocated;
	}
	
}
